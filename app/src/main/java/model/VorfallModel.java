package model;

public class VorfallModel {




    String schuelerName;
    String vergehenName;
    String strafpunkt;
    String zeit;

    public String getSchuelerName() {
        return schuelerName;
    }

    public void setSchuelerName(String schuelerName) {
        this.schuelerName = schuelerName;
    }

    public String getVergehenName() {
        return vergehenName;
    }

    public void setVergehenName(String vergehenName) {
        this.vergehenName = vergehenName;
    }

    public String getStrafpunkt() {
        return strafpunkt;
    }

    public void setStrafpunkt(String strafpunkt) {
        this.strafpunkt = strafpunkt;
    }

    public String getZeit() {
        return zeit;
    }

    public void setZeit(String zeit) {
        this.zeit = zeit;
    }


}
